#ifndef CONVERT_HPP
#define CONVERT_HPP

class Convert {
    public:
    static float c_to_f(float celsius); // convert celsius to fahrenheit
    static float c_to_k(float celsius); // convert celsius to kelvin
    static float f_to_c(float fahrenheit); // convert fahrenheit to celsius
    static float f_to_k(float fahrenheit); // convert fahrenheit to kelvin
    static float k_to_c(float kelvin); // convert kelvin to celsius
    static float k_to_f(float kelvin); // convert kelvin to fahrenheit
     
};




#endif